                    <p style="text-indent: 2em;">
	习近平总书记指出：“立足新发展阶段、贯彻新发展理念、构建新发展格局、推动高质量发展，必须深入实施科教兴国战略、人才强国战略、创新驱动发展战略，完善国家创新体系，加快建设科技强国，实现高水平科技自立自强。”</p>
<p style="text-indent: 2em;">
	党的十八大以来，以习近平同志为核心的党中央着眼全局、面向未来，作出“必须把创新作为引领发展的第一动力”的重大战略抉择，实施创新驱动发展战略，加快建设创新型国家，吹响建设世界科技强国的号角。</p>
<p style="text-indent: 2em;">
	十年踔厉奋发，我国科技创新事业发生历史性、整体性、格局性重大变化，走出了一条中国特色自主创新道路。</p>
<p style="text-indent: 2em;">
	<strong>加快科技自立自强，成功进入创新型国家行列</strong></p>
<p style="text-indent: 2em;">
	北京时间2022年9月1日，距离地球约400公里的浩瀚太空再次响起“我已出舱，感觉良好！”的自信话语。</p>
<p style="text-indent: 2em;">
	在亿万中国人的关注下，神舟十四号航天员以精彩表现，圆满完成首次“全新状态”出舱任务。仅仅16天后，神舟十四号乘组再次出舱，创造中国航天员两次出舱活动间隔时间最短纪录。</p>
<p style="text-indent: 2em;">
	以自立自强姿态攀登科技高峰的中国航天，是创新中国活力澎湃的一个生动写照。</p>
<p style="text-indent: 2em;">
	抓创新就是抓发展，谋创新就是谋未来。党的十八大正式提出实施创新驱动发展战略，党的十八届五中全会把创新放在五大发展理念之首，党的十九大提出创新是引领发展的第一动力，党的十九届五中全会强调坚持创新在我国现代化建设全局中的核心地位，把科技自立自强作为国家发展的战略支撑。</p>
<p style="text-indent: 2em;">
	一系列数据见证了中国创新的扎实脚印：全社会研发经费从1.03万亿元增长到2.79万亿元，居世界第二位；基础研究经费是十年前的3.4倍；科技人才队伍不断壮大，稳居世界第一位；2021年高被引论文数排名世界第二位，是2012年的5.4倍；全国高新技术企业数量从十多年前的4.9万家，增加到2021年的33万家……</p>
<p style="text-indent: 2em;">
	这十年，我国科技创新实力从量的积累迈向质的飞跃，从点的突破迈向系统能力提升，我国成功进入创新型国家行列，科技自立自强、建设科技强国的基础日益厚实。</p>
<p style="text-indent: 2em;">
	中国航天再次创造纪录，仅用33个月就实现第四个100次发射周期；自主研发设计的“华龙一号”示范工程全面建成投运，标志着我国核电技术水平和综合实力跻身世界第一方阵；主要农作物自主选育品种达到95％以上，农业科技进步贡献率超61%……</p>
<p style="text-indent: 2em;">
	这十年，重大自主创新成果涌现，中国人探索未知的脚步迈得更稳更远。</p>
<p style="text-indent: 2em;">
	中科院合肥物质科学研究院强磁场科学中心十年见证稳态强磁场这一“国之重器”从无到有，不断刷新世界纪录；科研人员在国际上首次在实验室实现了二氧化碳到淀粉的从头合成，被评价为“从0到1”的原创性突破；面向世界科技前沿，量子计算原型机、三维量子霍尔效应、纳米限域催化、凯勒几何核心猜想等取得重大突破……</p>
<p style="text-indent: 2em;">
	这十年，牢牢把握基础研究这一所有技术问题的“总开关”，原创性突破持续为科技创新提供源头活水。</p>
<p style="text-indent: 2em;">
	深海油气、煤炭清洁高效利用、新型核电技术为国家能源安全提供了有力保障；创新药物、国产高端医疗器械、先进诊疗技术让人民群众享受到更多高质量的创新成果；一批具有国际影响力的领军企业勇立创新潮头，683家企业进入2021年全球企业研发投入2500强……</p>
<p style="text-indent: 2em;">
	这十年，关键核心技术的攻坚突破，支撑国家重大需求，为保障产业链安全提供有力科技支撑。十年矢志创新，科技创新成为赋能发展、决胜未来的重要引擎、有力武器。</p>
<p style="text-indent: 2em;">
	<strong>创新第一动力不断增强，激发高质量发展内生动力</strong></p>
<p style="text-indent: 2em;">
	习近平总书记强调：“我们必须完整、准确、全面贯彻新发展理念，深入实施创新驱动发展战略，把科技的命脉牢牢掌握在自己手中，在科技自立自强上取得更大进展，不断提升我国发展独立性、自主性、安全性，催生更多新技术新产业，开辟经济发展的新领域新赛道，形成国际竞争新优势。”</p>
<p style="text-indent: 2em;">
	高质量发展是创新驱动的发展，创新是高质量发展的第一动力。</p>
<p style="text-indent: 2em;">
	矿井下，直径3.5米的采煤机1小时就能开采2500吨煤炭；5G远程操控，“一键”就能将原煤传送到地面；煤制油、煤制烯烃、煤制气以及超超临界高效发电等先进技术，让“乌金”物尽其用……从“傻大黑粗”到清洁高效，一粒煤的“华丽转身”，折射出的是十年来从要素驱动转向创新驱动的生动变化。</p>
<p style="text-indent: 2em;">
	单车型新能源汽车87天销量破万，这是赛力斯交出的转型“成绩单”。这家曾生产摩托车、面包车的企业，如今正在绿色低碳发展的赛道上奋力超越。</p>
<p style="text-indent: 2em;">
	这十年，粮食单产由亩产357公斤提高到2021年的387公斤，清洁能源消费量占比由14.5%提高到25.5%；人工智能、大数据等加快应用，太阳能光伏、新型显示等技术突破打通产业堵点……科技创新对经济社会发展的促进作用更趋强劲，为新旧动能转换注入活力。</p>
<p style="text-indent: 2em;">
	金沙江浩浩汤汤，流经白鹤滩水电站。今年5月，白鹤滩水电站最后一台百万千瓦机组顺利通过无水联合调试，向全面投产目标发起最后冲刺。</p>
<p style="text-indent: 2em;">
	这十年，北斗导航卫星全球组网，港珠澳大桥通车，锻压机、盾构机、发电机等国之重器接连下线，交通、能源、水利等领域捷报频传……科技创新支撑一批重大工程建设顺利实施，我国在战略性高技术领域积极抢占制高点。</p>
<p style="text-indent: 2em;">
	C919大型客机取得型号合格证。研制过程中，我国突破掌握了6000多项民用飞机技术，带动了整个航空工业的发展和进步。</p>
<p style="text-indent: 2em;">
	这十年，我国制造业增加值由16.98万亿元增长到31.4万亿元；4万多家“专精特新”中小企业、848家制造业单项冠军企业各怀绝技、携手并进；“中国制造”在全球产业链供应链中的影响力持续攀升……产业链创新链深度融合，不断将创新优势转化为产业优势、发展优势。</p>
<p style="text-indent: 2em;">
	从一根光纤起步，武汉东湖高新区的“中国光谷”成长为全球最大的光纤、光缆研发生产基地，覆盖“芯屏端网”的万亿元产业集群已具雏形。</p>
<p style="text-indent: 2em;">
	这十年，我国高技术产业营业收入从9.95万亿元增长到2021年的19.91万亿元，国家高新区园区生产总值占我国GDP比重从10.1%增长至13.4%，北京、上海、粤港澳大湾区三大国际科技创新中心位列全球科技创新集群前十……神州大地上，协同创新的引擎轰隆作响，一批世界级的产业集群加快崛起。</p>
<p style="text-indent: 2em;">
	<strong>坚持创新驱动发展，全面塑造发展新优势</strong></p>
<p style="text-indent: 2em;">
	用改革之手点燃创新引擎，科技创新动能更加强劲。</p>
<p style="text-indent: 2em;">
	浙江省金华市的浙江派尼尔科技股份有限公司，由于关键技术难以突破，一度面临被淘汰的风险。“揭榜挂帅”创新机制下，2020年5月，当地将这一难题纳入首批100个企业重大技术难题清单，向全球发出“英雄帖”。最终，上海工程技术大学科研团队前来揭榜，破解了企业生存困境，为其注入发展新活力。这是科技体制改革十年来持续深入的一个缩影。</p>
<p style="text-indent: 2em;">
	曾经管理过死的科研经费“活”起来了，“天女散花”式的科技项目“统”起来了，闲置的科研仪器“转”起来了……十年来，党中央对深化科技体制改革作出一系列重要战略部署，科技体制改革“四梁八柱”基本建立，重点领域和关键环节改革取得实质性突破，科技创新的基础性制度框架基本确立，助推国家创新体系整体效能显著提升。</p>
<p style="text-indent: 2em;">
	关键核心技术不断实现突破，产业链供应链更加自主可控。</p>
<p style="text-indent: 2em;">
	400吨重、3层楼高，牵引力堪比动车组。今年3月，随着自主研发的大吨位后驱刚性矿车XDE440下线，徐工机械成功摘得矿山设备这颗全球工程机械行业“皇冠上的明珠”。</p>
<p style="text-indent: 2em;">
	“奋斗者”号探底万米海沟、“中国天眼”捕捉宇宙脉动，一项项中国突破令人振奋；中国桥、中国路、中国车，一项项中国制造令人瞩目……十年来，广大科技工作者坚定不移走自主创新道路，在关键核心技术上奋力攻坚，重大创新成果不断涌现。关键核心技术攻关新型举国体制不断健全，进一步把主动权掌握在自己手中。</p>
<p style="text-indent: 2em;">
	为创新人才打造良好生态，人才作为支撑发展的第一资源作用更加充分。</p>
<p style="text-indent: 2em;">
	“整整5年里，我没有发表一篇与酵母相关的论文。”中科院分子植物科学卓越创新中心研究员覃重军坦言。正是对创新人才给予的信任，让他的团队一鸣惊人，在国际上首次人工创建单条染色体的真核细胞。</p>
<p style="text-indent: 2em;">
	十年来，我国不断完善科技创新人才体制机制，营造风清气正的科研创新生态，科技创新人才结构和质量不断优化、领军人才和创新团队加快涌现，为建成世界主要科学中心和创新高地打下坚实基础。</p>
<p style="text-indent: 2em;">
	今日中国，创新要素高效流动，创新动能更加澎湃。迈上全面建设社会主义现代化国家新征程，阔步前行的我们更加自信！</p>
